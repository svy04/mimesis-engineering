#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const [adapterArg = "gemini-cli", outputArg = ".mimesis/adapter-packets/gemini-cli.md"] = process.argv.slice(2);

const supportedAdapters = new Set(["gemini-cli", "claude-code"]);

if (!supportedAdapters.has(adapterArg)) {
  console.error(`Unsupported adapter: ${adapterArg}`);
  console.error(`Supported adapters: ${Array.from(supportedAdapters).join(", ")}`);
  process.exit(1);
}

const adapterPath = path.join(root, "adapters", `${adapterArg}.md`);
if (!fs.existsSync(adapterPath)) {
  console.error(`Missing adapter document: ${path.relative(root, adapterPath)}`);
  process.exit(1);
}

const outputPath = path.resolve(root, outputArg);
const outputDir = path.dirname(outputPath);
fs.mkdirSync(outputDir, { recursive: true });

function read(relativePath) {
  const fullPath = path.join(root, relativePath);
  if (!fs.existsSync(fullPath)) {
    return `Missing: ${relativePath}`;
  }
  return fs.readFileSync(fullPath, "utf8").trim();
}

const adapterDoc = read(`adapters/${adapterArg}.md`);
const packet = `# Mimesis CLI Packet: ${adapterArg}

Generated by:
\`tools/create-cli-packet.mjs\`

## Boundary

This packet prepares a Mimesis loop for ${adapterArg}.
It does not prove that ${adapterArg} was executed.
It does not include API keys, OAuth tokens, passwords, or private content.

## Adapter Contract

${adapterDoc}

## Current Spec Lock

${read(".mimesis/spec_lock.md")}

## Current Reference Set

${read(".mimesis/reference-set.md")}

## Current Structure Map

${read(".mimesis/structure-map.md")}

## Current Transformation Plan

${read(".mimesis/transformation-plan.md")}

## Current Boundary Check

${read(".mimesis/boundary-check.md")}

## Prompt To Use

\`\`\`text
Use Mimesis Engineering.

Do not roleplay expertise.
Use the references and structure maps as standards.
Transform the target artifact under the listed constraints.
Do not hide references.
Do not copy surface.
Do not invent proof.
Update the artifact trail before claiming completion.
\`\`\`
`;

fs.writeFileSync(outputPath, packet, "utf8");
console.log(`[packet] ${path.relative(root, outputPath).replaceAll(path.sep, "/")}`);
