#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const reportPath = path.join(root, ".mimesis", "security", "secret-safety-report.md");
const failures = [];
const findings = [];

const ignoredDirs = new Set([".git", "node_modules"]);
const allowedEnvNames = new Set([".env.example"]);
const riskyExactNames = new Set([
  ".env",
  ".env.local",
  ".env.production",
  ".npmrc",
  ".pypirc",
  "id_rsa",
  "id_dsa",
]);
const riskyExtensions = new Set([".pem", ".p12", ".pfx", ".key"]);

const tokenPatterns = [
  ["aws access key", /AKIA[0-9A-Z]{16}/],
  ["github token", /gh[pousr]_[A-Za-z0-9_]{20,}/],
  ["openai key", /sk-[A-Za-z0-9_-]{32,}/],
  ["google api key", /AIza[0-9A-Za-z_-]{30,}/],
  ["slack token", /xox[baprs]-[A-Za-z0-9-]{20,}/],
  ["private key block", /-----BEGIN [A-Z ]*PRIVATE KEY-----/],
];

const assignmentPattern = /\b(api[_-]?key|client[_-]?secret|secret|password|token|oauth[_-]?token)\b\s*[:=]\s*["']?([A-Za-z0-9._\-+/=]{16,})["']?/i;

function rel(filePath) {
  return path.relative(root, filePath).replaceAll(path.sep, "/");
}

function read(relativePath) {
  const fullPath = path.join(root, relativePath);
  if (!fs.existsSync(fullPath)) {
    failures.push(`missing ${relativePath}`);
    return "";
  }
  return fs.readFileSync(fullPath, "utf8");
}

function walk(dir, results = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      if (!ignoredDirs.has(entry.name)) {
        walk(path.join(dir, entry.name), results);
      }
      continue;
    }
    results.push(path.join(dir, entry.name));
  }
  return results;
}

function isTextFile(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  return [
    "",
    ".md",
    ".mjs",
    ".js",
    ".json",
    ".yml",
    ".yaml",
    ".txt",
    ".html",
    ".css",
    ".gitignore",
  ].includes(ext);
}

function isPlaceholderValue(value) {
  return /^(example|placeholder|redacted|dummy|test|changeme|not-a-secret|your_|xxx)/i.test(value) ||
    /abcdef/i.test(value) ||
    /^0+$/.test(value);
}

function scanFiles() {
  const files = walk(root);

  for (const file of files) {
    const base = path.basename(file);
    const ext = path.extname(file).toLowerCase();

    if (riskyExactNames.has(base) && !allowedEnvNames.has(base)) {
      findings.push(`${rel(file)}: risky credential filename`);
    }

    if (riskyExtensions.has(ext)) {
      findings.push(`${rel(file)}: risky private-key/certificate filename`);
    }

    if (!isTextFile(file)) {
      continue;
    }

    let content = "";
    try {
      content = fs.readFileSync(file, "utf8");
    } catch {
      continue;
    }

    const lines = content.split(/\r?\n/);
    for (let index = 0; index < lines.length; index += 1) {
      const line = lines[index];
      const lineRef = `${rel(file)}:${index + 1}`;

      for (const [label, pattern] of tokenPatterns) {
        if (pattern.test(line)) {
          findings.push(`${lineRef}: possible ${label}`);
        }
      }

      const assignment = line.match(assignmentPattern);
      if (assignment && !isPlaceholderValue(assignment[2])) {
        findings.push(`${lineRef}: possible secret assignment for ${assignment[1]}`);
      }
    }
  }
}

function writeReport() {
  const generated = `# Secret Safety Report

Status: ${findings.length ? "unsafe" : "no credential findings"}

Generated by \`npm run audit:secrets\`.

## Scope

This local audit scans text files for common credential patterns and risky credential filenames.

## Findings

${findings.length ? findings.map((finding) => `- ${finding}`).join("\n") : "- none"}

## Boundary

This audit is a local heuristic guardrail.
It does not prove production-grade security, complete secret detection, absence of private data, or legal compliance.
It does not upload, publish, commit, push, redact, rotate, or revoke credentials.
`;

  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, generated);
}

const packageJson = read("package.json");
const releaseCheck = packageJson.match(/"release:check":\s*"([^"]+)"/)?.[1] ?? "";
const doc = read("docs/SECRET-SAFETY-GATE.md");

if (!packageJson.includes('"audit:secrets"')) {
  failures.push("package.json missing script: audit:secrets");
}

if (!releaseCheck.includes("audit:secrets")) {
  failures.push("release:check must include npm run audit:secrets");
}

for (const text of [
  "does not prove production-grade security",
  "does not upload",
  "does not publish",
  "secrets",
]) {
  if (!doc.toLowerCase().includes(text.toLowerCase())) {
    failures.push(`docs/SECRET-SAFETY-GATE.md missing text: ${text}`);
  }
}

scanFiles();
writeReport();

if (findings.length) {
  failures.push(`secret safety scan found ${findings.length} risky item(s); see ${rel(reportPath)}`);
}

if (failures.length) {
  console.error("\nMimesis secret safety audit failed:");
  for (const failure of failures) {
    console.error(`[fail] ${failure}`);
  }
  process.exit(1);
}

console.log("Mimesis secret safety audit passed: no credential findings in local scan.");
